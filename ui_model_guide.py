import streamlit as st


def render_model_guide():
    """
    모델 A와 모델 B의 차이점을 설명하는 비교표를 렌더링합니다.
    """
    with st.expander("📖 분석 모델 비교 가이드", expanded=False):
        st.markdown("""
        ### 모델 A: 원인별 임팩트 분석
        - **목적**: 재무/감사용 표준 분석
        - **수량 차이**: (Q1−Q0) × P0 × ER0
        - **단가 차이**: (P1−P0) × Q1 × ER0
        - **환율 차이**: (ER1−ER0) × Q1 × P1
        - **특징**: 항등식 (①+②+③ = 총차이) 항상 성립
        
        ---
        
        ### 모델 B: 활동별 증분 분석
        - **목적**: 영업/전략 보고용 분석
        - **수량 차이**: 수량 증가 시 실적 단가, 감소 시 기준 단가 적용
        - **환율 차이**: P/Q 방향에 따른 4-Case 분기
        - **단가 차이**: 총차이 − 수량차이 − 환율차이 (Residual)
        - **특징**: 영업 활동의 기여도를 직관적으로 파악
        
        ---
        
        | 구분 | 모델 A | 모델 B |
        |------|--------|--------|
        | 용도 | 재무/감사 | 영업/전략 |
        | 수량 가격 | 기준 가격 | 방향별 가격 |
        | 잔차 | 없음 | 단가에 흡수 |
        """)
